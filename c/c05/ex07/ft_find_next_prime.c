#include <stdio.h>

int	ft_find_next_prime(int nb)
{
	long long i;

	if (nb <= 2)
		return (2);
	i = 2;
	while (i <= nb / i)
	{
		if (nb % i == 0)
		{
			i = 1;
			nb += 1;
		}
		i++;
	}
	return (nb);
}
