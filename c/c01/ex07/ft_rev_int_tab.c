void	ft_rev_int_tab(int *tab, int size)
{
	int	c;
	int i;

	i = 0;
	while (i < size - 1)
	{
		c = tab[size - 1];
		tab[size - 1] = tab[i];
		tab[i] = c;
		i++;
		size--;
	}
}
