#include <unistd.h>

void	ft_print_combn(int n)
{
	int arr[n + 1];
	int a;

	arr[0] = 0;
	a = n;
	if (a > 1)
	{
		while (a > 1)
		{
			arr[n - a + 1] = arr[n - a] + 1;
			a--;
		}
	}
}
